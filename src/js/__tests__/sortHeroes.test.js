import sortHeroesByHealth from '../sortHeroes';

test('sort heroes by health in descending order', () => {
  const input = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];
  const expected = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];
  const result = sortHeroesByHealth(input);

  expect(result).toEqual(expected);
});

test('check if the array is original', () => {
  const input = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];
  const copy = [...input];
  sortHeroesByHealth(input);
  expect(input).toEqual(copy);
});
